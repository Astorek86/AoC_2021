function SubmarineController()
	local ret = {}
	
	function ret.reset()
		ret.horizontal = 0
		ret.depth = 0
		ret.aim = 0
	end

	function ret.forward(vel)
		ret.horizontal = ret.horizontal + vel
	end
	
	function ret.forward2(vel)
		ret.forward(vel)
		ret.depth = ret.depth + ret.aim * vel
	end
	
	function ret.up(vel)
		ret.depth = ret.depth - vel
	end

	function ret.up2(vel)
		ret.aim = ret.aim - vel
	end
	
	function ret.down(vel)
		ret.depth = ret.depth + vel
	end

	function ret.down2(vel)
		ret.aim = ret.aim + vel
	end
	
	function ret.get_course()
		return ret.horizontal * ret.depth
	end
	
	ret.reset()
	return ret
end


